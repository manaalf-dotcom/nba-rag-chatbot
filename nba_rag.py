def generate_query_variations(question: str) -> list[str]:
    """Uses Gemini to generate 3 variations of the user's question."""
    prompt = f"""Generate 3 different search queries to find information about this question.
Return ONLY the 3 queries, one per line, no numbering, no extra text.

Original question: {question}

3 search queries:"""

    try:
        response = model.generate_content(prompt)
        variations = [q.strip() for q in response.text.strip().split("\n") if q.strip()]
        return [question] + variations[:3]  # original + 3 variations
    except:
        return [question]  # fallback to just original if it fails

def retrieve_multi_query(question: str, top_k=5) -> list:
    """
    Generates query variations, retrieves chunks for each,
    merges results and removes duplicates.
    """
    queries = generate_query_variations(question)
    print(f"  🔍 Searching with {len(queries)} query variations...")

    seen_ids = set()
    all_chunks = []

    for query in queries:
        results = retrieve(query, top_k=top_k)
        for chunk in results:
            # Use text as unique ID to deduplicate
            chunk_id = chunk["metadata"]["doc_id"] + str(chunk["metadata"]["chunk_idx"])
            if chunk_id not in seen_ids:
                seen_ids.add(chunk_id)
                all_chunks.append(chunk)

    # Sort by similarity score so best chunks come first
    all_chunks.sort(key=lambda x: x["score"], reverse=True)

    print(f"  📦 Retrieved {len(all_chunks)} unique chunks after merging")
    return all_chunks[:10]  # return top 10 unique chunks

