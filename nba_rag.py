# ── Chunking ──────────────────────────────────────────────────────────────────
def chunk_documents(pages, chunk_size=500, chunk_overlap=75):
    splitter = RecursiveCharacterTextSplitter(
        chunk_size=chunk_size, chunk_overlap=chunk_overlap,
        separators=["\n\n", "\n", ". ", " ", ""]
    )
    chunks = []
    for page in pages:
        for i, text in enumerate(splitter.split_text(page["content"])):
            chunks.append({"text": text, "metadata": {
                "doc_id": page["id"], "title": page["title"],
                "source": page["source"], "page": page["page"], "chunk_idx": i
            }})
    return chunks

# ── Ingestion ─────────────────────────────────────────────────────────────────
def ingest_chunks(chunks):
    ids       = [f"{c['metadata']['doc_id']}_c{c['metadata']['chunk_idx']}" for c in chunks]
    documents = [c["text"] for c in chunks]
    metadatas = [c["metadata"] for c in chunks]
    for i in range(0, len(chunks), 100):
        collection.upsert(
            ids=ids[i:i+100],
            documents=documents[i:i+100],
            metadatas=metadatas[i:i+100]
        )
    print(f"  ✅ Stored {len(chunks)} chunks — DB total: {collection.count()}")

# ── PDF ingestion ─────────────────────────────────────────────────────────────
def ingest_pdf(path):
    print(f"\n📥 Ingesting: {os.path.basename(path)}")
    doc = fitz.open(path)
    pages = []
    for page_num in range(len(doc)):
        text = doc[page_num].get_text("text").strip()
        if len(text) < 80:
            continue
        pages.append({
            "id":      f"{os.path.basename(path)}_p{page_num+1}",
            "title":   os.path.basename(path).replace(".pdf", ""),
            "source":  os.path.basename(path),
            "page":    page_num + 1,
            "content": text
        })
    doc.close()
    print(f"  📄 Extracted {len(pages)} pages")
    chunks = chunk_documents(pages)
    ingest_chunks(chunks)

# ── TXT ingestion with name enrichment ───────────────────────────────────────
def ingest_txt(filepath):
    with open(filepath, "r", encoding="utf-8") as f:
        content = f.read()

    filename   = os.path.basename(filepath)
    first_line = content.split("\n")[0].replace("PLAYER PROFILE: ", "").replace("TEAM PROFILE: ", "").replace("GAME LOG: ", "").replace("NBA LEAGUE LEADERS — ", "").strip()

    splitter = RecursiveCharacterTextSplitter(
        chunk_size=2000, chunk_overlap=200,
        separators=["\n\n", "\n", ". ", " ", ""]
    )
    raw_chunks = splitter.split_text(content)

    chunks = []
    for i, text in enumerate(raw_chunks):
        # Prepend name to every chunk so retrieval always connects numbers to the right player/team
        enriched_text = f"{first_line}\n{text}"
        chunks.append({"text": enriched_text, "metadata": {
            "doc_id":    filename,
            "title":     first_line,
            "source":    filename,
            "page":      1,
            "chunk_idx": i
        }})

    ingest_chunks(chunks)

# ── Retrieval ─────────────────────────────────────────────────────────────────
def retrieve(query, top_k=8):
    results = collection.query(
        query_texts=[query], n_results=top_k,
        include=["documents", "metadatas", "distances"]
    )
    return [{
        "text":     results["documents"][0][i],
        "metadata": results["metadatas"][0][i],
        "score":    1 - results["distances"][0][i]
    } for i in range(len(results["documents"][0]))]

