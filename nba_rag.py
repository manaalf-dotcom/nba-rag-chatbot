"""Define the chatbot with conversation memory"""

conversation_history = []
MAX_HISTORY = 10

def generate_with_retry(prompt, retries=3, wait=15):
    for attempt in range(retries):
        try:
            response = model.generate_content(prompt)
            return response.text.strip()
        except Exception as e:
            if "429" in str(e) and attempt < retries - 1:
                print(f"  ⏳ Rate limited — waiting {wait}s...")
                time.sleep(wait)
            else:
                raise e

def rag_chat(user_question: str) -> dict:
    # Retrieve top chunks
    chunks = retrieve(user_question, top_k=8)

    # Build context
    context = "\n---\n".join([
        f"[Source: {c['metadata']['source']}, Page: {c['metadata']['page']}]\n{c['text']}"
        for c in chunks
    ])

    # Build conversation history
    history_str = ""
    if conversation_history:
        history_str = "CONVERSATION HISTORY:\n"
        for exchange in conversation_history[-MAX_HISTORY:]:
            history_str += f"User: {exchange['question']}\n"
            history_str += f"Assistant: {exchange['answer']}\n\n"

    # Build prompt
    prompt = f"""You are an expert NBA chatbot. Answer the user's question using the context below.

IMPORTANT RULES:
- The context contains NBA stats tables. Each row is one season: Season, Team, GP, PTS, REB, AST, STL, BLK, FG%, 3P%, FT%
- PTS = points per game, REB = rebounds per game, AST = assists per game
- Read and interpret these numbers directly
- Always cite your sources as [Source: filename]
- Only say "I don't have that information" if the topic is genuinely absent

{history_str}
CONTEXT:
{context}

USER QUESTION: {user_question}

ANSWER:"""

    answer = generate_with_retry(prompt)

    # Save to memory
    conversation_history.append({"question": user_question, "answer": answer})
    if len(conversation_history) > MAX_HISTORY:
        conversation_history.pop(0)

    return {
        "answer":  answer,
        "sources": list(set([c['metadata']['source'] for c in chunks]))
    }

def chat(question: str):
    print(f"\n{'='*60}")
    print(f"You: {question}")
    print(f"{'='*60}")
    result = rag_chat(question)
    print(f"Bot: {result['answer']}")
    print(f"\n📚 Sources: {', '.join(result['sources'])}")
    print(f"{'='*60}\n")
    time.sleep(3)

