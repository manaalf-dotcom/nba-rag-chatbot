def safe_call(fn, *args, **kwargs):
    for attempt in range(3):
        try:
            time.sleep(0.6)
            return fn(*args, **kwargs)
        except Exception as e:
            print(f"    ⚠️  Attempt {attempt+1} failed: {e}")
            time.sleep(2)
    return None

